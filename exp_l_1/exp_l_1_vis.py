import pandas as pd

# Creating a list to hold our reformatted data
def json_to_csv(data, filename):
    rows = []
    for question, stats in data.items():
        row = {
            'questionname': question,
            'start_Mean': stats['start_Mean'],
            'start_SD': stats['start_SD'],
            'end_Mean': stats['end_Mean'],
            'end_SD': stats['end_SD'],
            'p-value': stats['p-value'],
            'trend': stats['Trend']
        }
        rows.append(row)

    # Converting list of dictionaries to DataFrame
    df = pd.DataFrame(rows)

    # Writing DataFrame to CSV
    df.to_csv(f'{filename}_ttest.csv', encoding='utf-8-sig', index=False)


